def extract_horsepower(text):
    m = re.search(r'([\d.]+)\s*HP', str(text), re.IGNORECASE)
    return float(m.group(1)) if m else np.nan

def extract_engine_liter(text):
    m = re.search(r'([\d.]+)\s*L', str(text))
    return float(m.group(1)) if m else np.nan

def extract_cylinders(text):
    m = re.search(r'(\d+)\s*Cylinder', str(text), re.IGNORECASE)
    if m:
        return float(m.group(1))
    m2 = re.search(r'(?:V|I|Straight\s*)(\d+)', str(text), re.IGNORECASE)
    return float(m2.group(1)) if m2 else np.nan

